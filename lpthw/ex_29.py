# boolean practice - dogs, cats, people

people = 20 
cats = 30 
dogs = 15 

if people < cats:
    print("too many cats! Meow!")

if people > cats:
    print("Not enough cats! Meow!")

if people > dogs:
    print("too many dogs! Woof!")

if people < dogs: 
    print("Not enough dogs! Woof!")

dogs += 5

if people >= dogs:
    print("people are greater than or equal to dogs")

if people <= dogs:
    print("people are less than or equal to dogs")

if people == dogs:
    print("people ARE dogs!")
